# 1.根据（1-7）的数值不同。打印对应的星期英文
day = float(input("请输入1-7中的一个数："))
if day == 1:
    print("Monday")
elif day == 2:
    print("Tuesday")
elif day == 3:
    print("Wednesday")
elif day == 4:
    print("Thursday")
elif day == 5:
    print("Friday")
elif day == 6:
    print("Saturday")
elif day == 7:
    print("Sunday")
else:
    print("输入格式错误！")

















